package teamfinder;

public class Passive {
	String name;
	String description;
	public Passive() {
		name = null;
		description = null;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return this.name;
	}
	
	public void setDescription(String description) {
		this.description = description;
	}
	
}
